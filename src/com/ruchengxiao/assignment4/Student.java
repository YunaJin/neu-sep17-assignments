package com.ruchengxiao.assignment4;
//This is solution for problem 4
public class Student {
    String name;
    long id;

    public Student(String name, long id) {
        this.name = name;
        this.id = id;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
